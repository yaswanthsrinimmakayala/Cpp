#include<iostream> // header file library which lets us to work with the input and output and objects
using namespace std;//means that we can use names for objects and variables from the standard library
int main()
{
    cout<<"HelloWorld!"<<endl;// cout doesn't create a new line at the end of the output we must use endl or \n if it is a string
    return 0;//it ends the main function
}
/*
 \n is new line character
 \t is a tab
 \ is used a escape symbol
 \" adds a " in the string
 \\ adds \ in the string
*/